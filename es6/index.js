//let variable example
let age = 23;
console.log(age);
age = 24;
console.log(age);

//const variable example
const userName = "sarvesh";
console.log(userName);
userName = "amk";
console.log(userName);

//Arrow function example
const greet = (user) => {
  console.log("Hello " + user);
};

greet("sarvesh");

//only return one statement
const add = (num) => num + 2;

add(2);
